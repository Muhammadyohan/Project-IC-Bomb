void buzzMelody() {
  for (int i = 0; i < sizeof(melody) / sizeof(melody[0]); i += 2) {
    int noteDuration = 1000 / (melody[i] / 2);
    tone(buzzPin, melody[i], noteDuration);
    delay(noteDuration + 30);
    noTone(buzzPin);
  }
}

